/**
 * 名前を匿名化するための関数
 * @param {string} originalName - 元の名前
 * @param {number} index - インデックス（0から始まる）
 * @param {boolean} isChild - 子供かどうか
 * @return {string} 匿名化された名前
 */
function getAnonymizedName(originalName, index, isChild = false) {
  if (!originalName) return "";

  const nameList = isChild ? CHILDREN_NAMES : HISTORICAL_NAMES;
  const listIndex = index % nameList.length; // 100名を超えたら循環

  return nameList[listIndex];
}
